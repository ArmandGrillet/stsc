        var maxValue = 0.0
        var keyForMaxValue = 0
        for ((k, v) <- qualities) {
            if (v > maxValue) {
                maxValue = v
                keyForMaxValue = k
            } else if (maxValue - 0.002 < v) {
                keyForMaxValue = k
            }
        }
        return keyForMaxValue
    }

